#include "ft_ssl.h"

uint32_t		f0(uint32_t *h)
{
	return ((h[1] & h[2]) | (~h[1] & h[3]));
}

uint32_t		f1(uint32_t *h)
{
	return ((h[3] & h[1]) | (~h[3] & h[2]));
}

uint32_t		f2(uint32_t *h)
{
	return (h[1] ^ h[2] ^ h[3]);
}

uint32_t		f3(uint32_t *h)
{
	return (h[2] ^ (h[1] | ~h[3]));
}
