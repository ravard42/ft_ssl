#include "ft_ssl.h"

uint32_t		sh_l(uint32_t x, uint8_t n)
{
	return ((x) << (n));
}

uint32_t		sh_r(uint32_t x, uint8_t n)
{
	return ((x) >> (n));
}

uint32_t		rot_l(uint32_t x, uint8_t n)
{
	return (((x) << (n)) | ((x) >> (32 - (n))));
}

uint32_t		rot_r(uint32_t x, uint8_t n)
{
	return (((x) >> (n)) | ((x) << (32 - (n))));
}
