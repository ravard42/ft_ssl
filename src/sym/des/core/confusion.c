#include "ft_ssl.h"

uint32_t			s_boxes(uint64_t x)
{
	uint32_t	y;
	int8_t		i;

	y = 0;
	i = -1;
	while (++i < 8)
		y |= (uint32_t)g_s[i][(x >> (58 - 6 * i)) & 63]
			<< ((7 - i) * 4);
	return (y);
}
